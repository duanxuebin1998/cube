/* 显示数据 */
uint8_t OledValueDisplay(int value,uint8_t line,uint8_t row,uint8_t shift,uint8_t points,uint8_t* unit)
{
    uint8_t deci;
    int sgn = 1;
    //求出要显示的数据的总位数及符号
    deci = judgedecimals(value);
    if(value < 0)
    {
        sgn = -1;
        value = abs(value);
    }
    //显示数值
    line = dis_number(sgn,value,points,line,row,shift,deci);
    //显示单位
    if(unit != NULL)
        line = OledDisplayLineWords(unit,line,row,shift);
    return line;
}
/* 计算数值位数 */
static uint8_t judgedecimals(int value)
{
    uint8_t cnt = 1;
    uint32_t number;

    number = abs(value);
    while(number / 10)
    {
        cnt++;
        number /= 10;
    }
    return cnt;
}
/* 显示数字 */
static uint8_t dis_number(int sgn,int value,int points,uint8_t line,uint8_t row,uint8_t shift,uint8_t deci)
{
    int i;
    int n;

    //若有负号先显示出负号
    if(sgn == -1)
        line = OledDisplayOneNmb(11,row,line,shift);
    //显示数字
    if(deci <= points)
        line = OledDisplayOneNmb(0,row,line,shift);
    
    for(i = deci - 1;i >= points;i--)
    {
        n = value / pow(10,i) ;
        line = OledDisplayOneNmb(n,row,line,shift);
        value %= (uint32_t)pow(10,i);
    }
    if(points > 0)
    {
        line = OledDisplayOneNmb(10,row,line,shift);
        line -= 2;
        for(i = points - 1;i >= 0;i--)
        {
            n = value / pow(10,i) ;
            line = OledDisplayOneNmb(n,row,line,shift);
            value %= (uint32_t)pow(10,i);
        }
    }
    return line;
}
