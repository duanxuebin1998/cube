/* 计算要显示的参数个数 */
static void CalculateValidPara(void)
{
    static int ValidParaCnt = 0;//需要显示的有效参数
    static u8 start_x = 0;
    static int rowsperpage = 2;
    int i;
    
    /******计算总共有多少个有效参数需要显示******/
    ValidParaCnt = 0;
    //液位
    if((inputValue.OilLevel == UNVALID_LEVEL )||(inputValue.OilLevel == 0 ))
    {
        flagofoillevelvalid = false;
    }
    else
    {
        flagofoillevelvalid = true;
    }
    //密度
    if(!(inputValue.SPM_Density == UNVALID_DENSITY ))
    {
        ValidParaCnt++;
        ValidParaDisArr[Para_AveDensity][PARA_NUM] = ValidParaCnt;
		
        ValidParaDisArr[Para_AveDensity][PARA_VALID] = true;
    }
    else
        ValidParaDisArr[Para_AveDensity][PARA_VALID] = false;
    //温度
    if((inputValue.SPM_Temperature > UNVALID_TEMPERATURE_WIRELESS )&&(inputValue.SPM_Temperature <2000 ))
    {
        ValidParaCnt++;
        ValidParaDisArr[Para_AveTemperature][PARA_NUM] = ValidParaCnt;
        ValidParaDisArr[Para_AveTemperature][PARA_VALID] = true;
    }
    else
        ValidParaDisArr[Para_AveTemperature][PARA_VALID] = false;
    //水位
    if(!(inputValue.WaterLevel == LEVEL_DOWNLIMITWATER ))
    {
        ValidParaCnt++;
        ValidParaDisArr[Para_Waterlevel][PARA_NUM] = ValidParaCnt;
        ValidParaDisArr[Para_Waterlevel][PARA_VALID] = true;
    }
    else
        ValidParaDisArr[Para_Waterlevel][PARA_VALID] = false;
  
    //位置
    if((!(inputValue.EquipState == STATE_FLOWOIL))&&(inputValue.RealtimePosition != 0 ))
    {
        ValidParaCnt++;
        ValidParaDisArr[Para_position][PARA_NUM] = ValidParaCnt;
        ValidParaDisArr[Para_position][PARA_VALID] = true;
    }
    else
        ValidParaDisArr[Para_position][PARA_VALID] = false;
  
    #if DEBUG_DISPLAY
    printf("ValidParaCnt = %d\n",ValidParaCnt);
    #endif
    /******计算总共需用几页,以及起始行******/
    if(flagofoillevelvalid == true)
    {
        rowsperpage = 2;
        PageAmount = ceil(ValidParaCnt / (float)rowsperpage);
        start_x = OLED_ROW4_3;
    }
    else
    {
        rowsperpage = 3;
        PageAmount = ceil(ValidParaCnt / (float)rowsperpage);
        start_x = OLED_ROW4_2;
    }
    #if DEBUG_DISPLAY
    printf("rowsperpage = %d\n",rowsperpage);
    printf("PageAmount = %d\n",PageAmount);
    printf("start_x = %d\n",start_x);
    #endif
    /******计算每一个参数对应的行号和页号******/
    if(PageAmount == 0 && rowsperpage == 2)//特殊情况:一共只显示两行的情况下这两行要居中显示
    {
        FlagofTotalTwoRow = true;
    }
    else
    {
        FlagofTotalTwoRow = false;
        for(i = 0;i<Para_Amount;i++)
        {
            ValidParaDisArr[i][PARA_X] = ((ValidParaDisArr[i][PARA_NUM] - 1) % rowsperpage ) * (OLED_ROW4_2 - OLED_ROW4_1) + start_x;
            ValidParaDisArr[i][PARA_PAGE] = (ValidParaDisArr[i][PARA_NUM] - 1) / rowsperpage;
            #if DEBUG_DISPLAY
            printf("%d\tx = %d\tpage = %d\tnum = %d\n",i,ValidParaDisArr[i][PARA_X],ValidParaDisArr[i][PARA_PAGE],ValidParaDisArr[i][PARA_NUM]);
            #endif
        }
    }
}

