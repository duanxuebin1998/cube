/*
**功能：显示汉字和字符
**参数：display - 指向要显示的字符串的指针的指针
        cnt - 显示汉字的个数
        x - 屏幕横坐标
        y - 屏幕纵坐标
        shift - 阴码阳码选项
**返回值：无
*/
static uint8_t matchingwordstock(uint8_t **display,int cnt,int x,int y,int shift)
{
    int i,j;
    const uint8_t *pdisplay;
    uint8_t *pstockmap;

    pdisplay = *display;/*指向要显示的字符串首地址*/
    for(i = 0;i < cnt;)
    {
        if(x >= OLED_LINE8_END)
            break;
        if((*pdisplay) < 128)
        {
            pstockmap = CharStockMap;
            for(j=0;j<CharStockMapLength;j++)
            {
                if(*pstockmap == *pdisplay)
                {
                    write_816(x,y,CharStock,j,shift);
                }
                pstockmap++;
            }
            x += 4;
            pdisplay++;
            i++;
        }
        else
        {
            pstockmap = StockMap;/*指向索引字库首地址*/
            /*遍历字库中是否有要显示的汉字*/
            for(j = 0;j< StockmapLength ;j++)
            {
                if((*pstockmap == *pdisplay) && (*(pstockmap + 1)== *(pdisplay + 1)))
                {
                    if(j < 255)//第一个字库255汉字
                    {
                        write_hanzi16(x,y,WordStock,j,j + 1,shift);
                    }
                    else 
                    {
                        write_hanzi16(x,y,WordStock2,j - 255,j + 1 - 255,shift);
                    }
                    break;
                }
                /*一个汉字占两个字节,所以索引指针每次加2*/
                pstockmap += wordbyte;
            }
            /*无论字库是否包含要显示的字,位置都向右移,字库中没有的空出位置*/
            x += 7;
            pdisplay += wordbyte;
            i += wordbyte;
        }
    }
    return x;
}
/* 显示多个汉字或字符 */
uint8_t OledDisplayLineWords(uint8_t* data,uint8_t x,uint8_t y,uint8_t shift)
{
    int len = 0;
    if(data == NULL) 
        return y;
    while(*(data + len) != 0)
    {
        len++;
    }
    y = matchingwordstock(&data,len,x,y,shift);
    return y;
}
