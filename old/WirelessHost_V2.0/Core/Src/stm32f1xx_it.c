/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	GPIO_PinState bitstatus;
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
  bitstatus = HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_11);
  if (bitstatus == receivedDataBuffer.ioState[receivedDataBuffer.currentBit])  // P1.5 引脚的状态与上次采集相等的处理�?�辑
  {
      receivedDataBuffer.duration[receivedDataBuffer.currentBit]++;
      if( receivedDataBuffer.duration[receivedDataBuffer.currentBit]>BPM_MAX_DELAY)
      {
          receivedDataBuffer.bpm_state =BPM_RECEIVE_COMPLETE_STATE;//单个字节接收完成
          // 停止定时
          HAL_TIM_Base_Stop_IT(&htim2);
          __HAL_TIM_CLEAR_FLAG(&htim2,TIM_FLAG_UPDATE);
      }
  }
  else
  {
      receivedDataBuffer.currentBit ++;
      receivedDataBuffer.ioState[receivedDataBuffer.currentBit] = bitstatus;
      receivedDataBuffer.duration[receivedDataBuffer.currentBit] =1;
  }
  /* USER CODE END TIM2_IRQn 1 */
}

