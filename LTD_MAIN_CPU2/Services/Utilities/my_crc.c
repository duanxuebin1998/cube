/* 接收到的数据包进行CRC校验 */
bool SlaveCheckCRC(uint8_t const *revframe, int framelen) {
    unsigned char Hi, Lo;
    unsigned short crc;

    // 修复类型不匹配问题
    crc = CRC16_Calculate((const unsigned char *)revframe, framelen - 2);

    Lo = crc & 0x00ff;
    Hi = crc >> 8;

    if ((Hi != revframe[framelen - 1]) || (Lo != revframe[framelen - 2])) {
        printf("CRC Error: Calc Hi=%02x, Lo=%02x | Frame Hi=%02x, Lo=%02x\r\n",
              Hi, Lo,
              (unsigned char)revframe[framelen - 1],
              (unsigned char)revframe[framelen - 2]);
        return false;
    }
    return true;
}


/* lenBytes 必须是 4 的倍数，不足时用 0 填充 */
uint32_t CRC32_HAL(const uint8_t *buf, uint32_t lenBytes) {
	uint32_t word, crc;
	uint32_t words = lenBytes >> 2;                // HAL 接口的长度单位是“32-bit word” :contentReference[oaicite:3]{index=3}
	crc = HAL_CRC_Calculate(&hcrc, (uint32_t*) buf, words);

	/* 处理尾部 1‒3 个字节 */
	uint8_t remain = lenBytes & 3U;
	if (remain) {
		word = 0;
		memcpy(&word, buf + (words << 2), remain);
		crc = HAL_CRC_Accumulate(&hcrc, &word, 1);
	}

	return crc;   // HAL 已做反射，这里做最后一步取反
}
/* --------------- 测试用例表 -------------- */
typedef struct {
	const char *name; /* 用例名 */
	const uint8_t *data; /* 数据指针 */
	uint32_t len; /* 数据长度（字节） */
	uint32_t expect; /* 期望 CRC32 */
} CRC_TestCase;

static const uint8_t vector1[] = "123456789"; /* 0xCBF43926 */
static const uint8_t vector2[] = { 0x00 }; /* 0xD202EF8D */
static const uint8_t vector3[] = { 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A }; /* 0xE3069283 */

static const CRC_TestCase kCases[] = { { "ASCII 123456789", vector1, sizeof(vector1) - 1, 0xAFF19057 }, { "Single 0x00 Byte", vector2, sizeof(vector2),
		0xC704DD7B }, { "10‑byte Incremental", vector3, sizeof(vector3), 0xE8008411 }, };
#define CASE_COUNT (sizeof(kCases)/sizeof(kCases[0]))

/* ---------------- 测试主函数 ------------- */
void CRC32_HAL_Test(void) {
	uint32_t ok = 0;

	printf("\r\n===== CRC32_HAL 单元测试开始 =====\r\n");
	for (uint32_t i = 0; i < CASE_COUNT; ++i) {
		const CRC_TestCase *c = &kCases[i];
		uint32_t crc = CRC32_HAL(c->data, c->len);

		printf("[%lu] %s : 0x%08lX  (期望 0x%08lX)  ... ", (unsigned long) i, c->name, (unsigned long) crc, (unsigned long) c->expect);

		if (crc == c->expect) {
			printf("PASS\r\n");
			++ok;
		} else {
			printf("FAIL\r\n");
		}
	}

	printf("===== 全部 %lu/%lu 用例通过! =====\r\n", (unsigned long) ok, (unsigned long) CASE_COUNT);
}
