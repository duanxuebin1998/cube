/**
 * @brief CRC16/MODBUS 校验计算
 * @param data 输入数据指针
 * @param length 数据长度（字节）
 * @return 16位CRC校验值
 */
uint16_t CRC16_Calculate(const uint8_t *data, uint32_t length) {
	uint8_t crc_hi = 0xFF;
	uint8_t crc_lo = 0xFF;

	while (length--) {
		uint8_t index = crc_lo ^ *data++;
		crc_lo = crc_hi ^ auchCRCHi[index];
		crc_hi = auchCRCLo[index];
	}

	return (crc_hi << 8) | crc_lo;
}
